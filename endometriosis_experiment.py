# Function to convert DICOM to PNG
def dicom_to_png(dicom_path, output_path):
    try:
        # Read the DICOM file
        dicom_image = pydicom.dcmread(dicom_path)

        # Extract pixel array from DICOM file
        img_array = dicom_image.pixel_array

        # Normalize pixel values (0 to 255) and convert to uint8
        img_array = cv2.normalize(img_array, None, 0, 255, cv2.NORM_MINMAX)
        img_array = img_array.astype('uint8')

        # Save as PNG
        cv2.imwrite(output_path, img_array)
        print(f"Saved {output_path}")
    except Exception as e:
        print(f"Error converting {dicom_path}: {e}")

# Loop through all patient subfolders in the main directory
for patient_folder in os.listdir(dicom_dir):
    patient_folder_path = os.path.join(dicom_dir, patient_folder)

    # Check if the path is a directory (to ignore any other files)
    if os.path.isdir(patient_folder_path):
        # Loop through all DICOM files in the patient's folder
        for filename in os.listdir(patient_folder_path):
            if filename.endswith(".dcm"):  # Check if the file is a DICOM file
                dicom_path = os.path.join(patient_folder_path, filename)

                # Define the output path for the PNG image (include patient folder name for clarity)
                output_path = os.path.join(output_dir, f"{patient_folder}_{filename.replace('.dcm', '.png')}")

                # Convert and save the image
                dicom_to_png(dicom_path, output_path)

