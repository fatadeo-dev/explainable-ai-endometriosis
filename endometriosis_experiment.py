import os
import cv2
import imgaug.augmenters as iaa

# Define directories for resized and augmented images
resized_dir = '/content/resized_images/'
augmented_dir = '/content/augmented_images/'

# Create the augmented directory if it does not exist
if not os.path.exists(augmented_dir):
    os.makedirs(augmented_dir)

# Define augmentation sequence
seq = iaa.Sequential([
    iaa.Fliplr(0.5),  # Flip horizontally 50% of the time
    iaa.Rotate((-30, 30)),  # Rotate between -30 and 30 degrees
    iaa.Affine(translate_percent={"x": (-0.1, 0.1), "y": (-0.1, 0.1)}),  # Shift by -10% to 10%
    iaa.Multiply((0.8, 1.2)),  # Change brightness
    iaa.AdditiveGaussianNoise(scale=(0, 0.1*255))  # Add Gaussian noise
])

# Function to augment images
def augment_image(input_path, output_path):
    img = cv2.imread(input_path)  # Read the image
    img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)  # Convert to RGB format
    augmented_images = seq(images=[img] * 5)  # Create 5 augmented versions
    for i, aug_img in enumerate(augmented_images):
        aug_img = cv2.cvtColor(aug_img, cv2.COLOR_RGB2BGR)  # Convert back to BGR format for saving
        cv2.imwrite(os.path.join(output_path, f"aug_{i}_{os.path.basename(input_path)}"), aug_img)

# Loop through all images in the resized directory and augment them
for filename in os.listdir(resized_dir):
    if filename.endswith(".png"):  # Check if the file is a PNG image
        input_path = os.path.join(resized_dir, filename)

        # Define the output path for the augmented images
        augment_image(input_path, augmented_dir)

# Print the number of augmented files
augmented_files = os.listdir(augmented_dir)
print(f"Number of augmented files: {len(augmented_files)}")
print("Files in the augmented directory:", augmented_files)

import os
import cv2
import numpy as np

# Define directories for augmented and normalized images
augmented_dir = '/content/augmented_images/'
normalized_dir = '/content/normalized_images/'

# Create the normalized directory if it does not exist
if not os.path.exists(normalized_dir):
    os.makedirs(normalized_dir)

# Function to normalize images
def normalize_image(input_path, output_path):
    img = cv2.imread(input_path)  # Read the image
    img = img.astype('float32')  # Convert to float32 for normalization
    img /= 255.0  # Normalize to [0, 1]
    cv2.imwrite(output_path, img * 255)  # Save as uint8 by scaling back to [0, 255]
    print(f"Normalized and saved {output_path}")

# Loop through all images in the augmented directory and normalize them
for filename in os.listdir(augmented_dir):
    if filename.endswith(".png"):  # Check if the file is a PNG image
        input_path = os.path.join(augmented_dir, filename)

        # Define the output path for the normalized image
        output_path = os.path.join(normalized_dir, filename)

        # Normalize and save the image
        normalize_image(input_path, output_path)

# Print the number of normalized files
normalized_files = os.listdir(normalized_dir)
print(f"Number of normalized files: {len(normalized_files)}")
print("Files in the normalized directory:", normalized_files)

